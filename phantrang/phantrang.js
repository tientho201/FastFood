var thispage = 1;
var limit = 8;

var currentArray = a; // Mảng mặc định là a

function loaditem() {
    var begin = limit * (thispage - 1);
    var end = limit * thispage - 1;

    var s = '';

    currentArray.map((value, index) => {
        if (index >= begin && index <= end) {
            s += '<div class="item"><span ><img class="anh" src="' + value.img + '"></span><ul class="thongtinsp"><li>Productid:' + value.IDproduct + '</li><li>Brand:' + value.item +
                '</li><li>Name:' + value.name + '</li><li>Price:' + value.gia + '</li</ul></div>';
        }
        return s;
    });

    document.querySelector(".hienthi").innerHTML = s;
    listpage();
}

function listpage() {
    var perpage = Math.ceil(currentArray.length / limit);
    var buttonsHTML = '';

    for (var i = 1; i <= perpage; i++) {
        buttonsHTML += '<input type="button" class="sotrang" value="' + i + '" onclick="changepage(' + i + ')"></input>';
    }

    document.querySelector('.chuyentrang').innerHTML = buttonsHTML;
}

function changepage(i) {
    thispage = i;
    loaditem();
}

